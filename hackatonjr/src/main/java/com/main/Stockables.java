public class stockables {
    private String nom;
    private float prix;
    public stockables(String nom, float prix) {
        this.nom = nom;
        this.prix = prix;
    }

    public String Getnom() {
        return nom;
    }
    public float Getprix() {
        return prix;
    }
    public void Setnom(String nom) {
        this.nom = nom;
    }
    public void Setprix(float prix) {
        this.prix = prix;
    } 




}


